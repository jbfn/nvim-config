local set = vim.opt

-- Set default indent
set.shiftwidth = 4

-- Highlight line cursor is on
set.cursorline = true

-- Show relative line numbers
set.number = true
set.relativenumber = true

-- Show vertical line at line wrap
set.colorcolumn = { 100 }

-- Highlight matching characters when typing in search
set.incsearch = true

-- Make search case insensitive
set.ignorecase = true

-- Override ignorecase when searching for capital letters
set.smartcase = true
